DROP VIEW IF EXISTS faraodaily
;
CREATE VIEW IF NOT EXISTS faraodaily(
  hunt_date,
  daily_hunt_count,
  total_hunt_count,
  drop01,
  drop02,
  drop03,
  drop04,
  drop05,
  drop06,
  drop07,
  drop08,
  drop09,
  drop10,
  drop11,
  drop12,
  drop13,
  drop14,
  drop15,
  drop16,
  drop17,
  drop18,
  drop19,
  drop20
) AS
SELECT
  strftime(
    '%Y/%m/%d',
    hunt_date
  ),
  COUNT(*),
  MAX(
    hunt_seq
  ),
  SUM(
    drop01
  ),
  SUM(
    drop02
  ),
  SUM(
    drop03
  ),
  SUM(
    drop04
  ),
  SUM(
    drop05
  ),
  SUM(
    drop06
  ),
  SUM(
    drop07
  ),
  SUM(
    drop08
  ),
  SUM(
    drop09
  ),
  SUM(
    drop10
  ),
  SUM(
    drop11
  ),
  SUM(
    drop12
  ),
  SUM(
    drop13
  ),
  SUM(
    drop14
  ),
  SUM(
    drop15
  ),
  SUM(
    drop16
  ),
  SUM(
    drop17
  ),
  SUM(
    drop18
  ),
  SUM(
    drop19
  ),
  SUM(
    drop20
  )
FROM
  faraohunt
GROUP BY
  strftime(
    '%Y/%m/%d',
    hunt_date
  )
;
